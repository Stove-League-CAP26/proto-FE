export type PositionGroup = "투수" | "포수" | "내야수" | "외야수";

export interface RosterPlayer {
  pid: number;
  name: string;
  number: number;
  position: string; // SP, RP, CL, C, 1B, 2B, 3B, SS, LF, CF, RF, DH
  positionGroup: PositionGroup;
  bats: "L" | "R" | "S";
  throws: "L" | "R";
}

/** PlayerProfilePage 와 동일한 이미지 URL 방식 */
export const getPlayerImageUrl = (
  pid: number,
  season: 2024 | 2025 = 2025,
): string => `https://www.koreabaseball.com/DATA/Player/${season}/${pid}.gif`;

