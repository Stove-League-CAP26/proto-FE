// 2024 로스터 = 2025에서 일부 차이 (간소화: 2025와 동일로 처리, 실제로는 별도 데이터 필요)
const ROSTERS_2024 = TEAM_ROSTERS.map((r) => ({ ...r, season: 2024 as const }));
export const ALL_ROSTERS = [...TEAM_ROSTERS, ...ROSTERS_2024];

/** teamId + season 기준 로스터 반환 */
export function getTeamRoster(
  teamId: string,
  season: 2024 | 2025,
): RosterPlayer[] {
  const found = ALL_ROSTERS.find(
    (r) => r.teamId === teamId && r.season === season,
  );
  return found?.players ?? [];
}

/** positionGroup 기준 그룹핑 */
export function groupByPosition(
  players: RosterPlayer[],
): Record<PositionGroup, RosterPlayer[]> {
  const groups: Record<PositionGroup, RosterPlayer[]> = {
    투수: [],
    포수: [],
    내야수: [],
    외야수: [],
  };
  players.forEach((p) => groups[p.positionGroup].push(p));
  return groups;
}
